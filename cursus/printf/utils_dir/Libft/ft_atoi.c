#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	res;
	int	flag;

	i = 0;
	while (nptr[i] == '\t' || nptr[i] == '\n' || nptr[i] == '\r'
		|| nptr[i] == '\v' || nptr[i] == '\f' || nptr[i] == ' ')
		i++;
	flag = 1;
	if (nptr[i] == '-')
		flag *= -1;
	if (nptr[i] == '+' || nptr[i] == '-')
		i++;
	res = 0;
	while (ft_isdigit(nptr[i]))
	{
		res = res * 10 + nptr[i] - '0';
		i++;
	}
	return (res * flag);
}
